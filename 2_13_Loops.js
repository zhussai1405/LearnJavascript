"use strict"
//while loop with i++
let i = 0;
while (i < 5) { 
  console.log('Loop iteration start 0 to 5: ' + i );
  i++;
}

// when j become j-- reverse the loop
let j = 5;
while (j) { 
  console.log('Loop start in decrement operator: ' + j);
  j--;
}

// Do while loop 
let k = 0;
do {
  console.log('do while loop: ' + k);
  k++;
} while (k < 3);

//for loop
let l = 0;

for (l = 0; l < 3; l++) { 
  console.log(l); 
}

console.log('for Loop' + l); 


